					<h3>Le Bento</h3>

					<p>La cuisine japonaise présente de nombreux avantages diététiques, en termes d’ingrédients mais aussi de préparation. La façon de se nourrir et les règles qui entourent le repas participent également à faire de l’alimentation japonaise un exemple nutritionnel.</p>
	                
	                <p>Le concept du bento s’inscrit dans cette mouvance culinaire «&nbsp;tendance&nbsp;» et «&nbsp;saine&nbsp;» avec en plus, une façon originale de consommer. Le Bento-Bako 弁当箱 (Boîte à bento) vous offre en effet un aspect unique jouant sur l’opposition des couleurs, des saveurs et des textures.</p>
